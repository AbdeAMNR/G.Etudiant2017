    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        try {
            stm = cnx.obtenirconnexion().createStatement();
            rs = stm.executeQuery("SELECT * FROM etudiant_info WHERE nom_prenom ='"
                    + "" + this.txtNomPrenom.getText() + "'");
            if (!rs.first()) {
                String annee_naissance = "2000-01-01";
                String promo = "2000";
                if (!LoginFrame.isNullOrWhiteSpace(this.txtNomPrenom.getText())) {
                    String nomPrenom = this.txtNomPrenom.getText();
                    if (this.dateChooserNaissance.getDate() != null) {
                        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                        annee_naissance = (String) sdf.format(this.dateChooserNaissance.getDate());
                    }
                    String fili = (String) this.cbxFiliere.getSelectedItem();
                    int filiere = getIdFiliere(fili);
                    if (this.yearChooserPromo.getDate() != null) {
                        SimpleDateFormat sdfyear = new SimpleDateFormat("yyyy");
                        promo = (String) sdfyear.format(this.yearChooserPromo.getDate());
                    }
                    String genre = (String) this.cbxGenre.getSelectedItem();
                    String adresse = this.txtAdresse.getText();
                    String tele = this.txtTele.getText();
                    String email = this.txtEmail.getText();
                    String rqtInsert = "INSERT INTO etudiant_info (nom_prenom , annee_naissance ,filiere,promo,  genre , adresse ,  tele , email ) "
                            + "VALUES ('" + nomPrenom + "','" + annee_naissance + "'," + filiere + ",'" + promo + "','" + genre + "','" + adresse + "','" + tele + "','" + email + "')";
                    System.out.println(rqtInsert);
                    int c = cnx.update(rqtInsert);

                    chargerTable();
                    JOptionPane.showMessageDialog(null, c + " Enregistrement bien ajouter", "Etat de L'operation", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Champ vide ou il y a des espaces", "Invalide", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "already exist", "Invalide", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            System.err.println(ex);
        } finally {
            try {
                rs.close();
                stm.close();
            } catch (SQLException ex) {
                Logger.getLogger(EtudiantInfos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btAddActionPerformed
    public void runSearch() {
        try {
            String req = "SELECT e.Etudiant_id, e.nom_prenom, "
                    + "e.annee_naissance,f.filiere_nom,e.promo,e.genre,"
                    + "e.adresse,e.tele,e.email FROM etudiant_info e "
                    + "join filiere f on e.filiere= f.Filiere_id"
                    + " where e." + this.cbxRecherche.getSelectedItem()
                    + " like '%" + this.txtSearch.getText() + "%'";
            System.out.println(req);

            Statement stm2 = cnx.obtenirconnexion().createStatement();
            ResultSet Rs = stm2.executeQuery(req);

            model.setRowCount(0);
            while (Rs.next()) {
                model.addRow(new Object[]{
                    Rs.getInt("e.Etudiant_id"), Rs.getString("e.nom_prenom"),
                    Rs.getDate("e.annee_naissance"), Rs.getString("f.filiere_nom"),
                    Rs.getDate("e.promo"), Rs.getString("e.genre"),
                    Rs.getString("e.adresse"), Rs.getString("e.tele"),
                    Rs.getString("e.email")
                });
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
        this.jTable1.setModel(model);
    }
