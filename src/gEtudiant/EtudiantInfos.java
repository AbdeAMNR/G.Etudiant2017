    private void getPhoto() {
        int row = jTable1.getSelectedRow();
        int value = (int) jTable1.getValueAt(row, 0);
        try {
            stm = cnx.obtenirconnexion().createStatement();
            rs = stm.executeQuery("SELECT e.photo FROM etudiant_info e WHERE e.Etudiant_id=" + value);
            if (rs.first()) {
                String link = rs.getString("photo");
                if (rs.wasNull() && rs.getObject("photo") == null) {
                    setImageToDefault();
                } else {
                    File fichier = new File(link);
                    Image image = ImageIO.read(fichier);
                    lblPhoto.setIcon(new ImageIcon(image));
                }
            }
        } catch (IOException | SQLException ex) {
            System.err.println(ex);
        }
    }

