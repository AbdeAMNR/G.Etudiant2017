    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int OptionChoisie = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer les informations de cet étudiant?", "Attendez une seconde", JOptionPane.YES_NO_OPTION);
        if (OptionChoisie == 0) {
            int row = -1;
            row = jTable1.getSelectedRow();
            /*check if row selected in jtable*/
            if (row != -1) {
                int value = (int) jTable1.getValueAt(row, 0);
                String del_Query = "DELETE FROM etudiant_info WHERE Etudiant_id= " + value;
                System.out.println(del_Query);
                int effects = cnx.update(del_Query);
                System.out.println(effects + " deleted row(s)");
                chargerTable();
            } else {
                JOptionPane.showMessageDialog(this, "error", "Please select a student ", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

