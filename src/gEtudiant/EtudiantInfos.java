    public final void chargerTable() {
        try {
            stm = cnx.obtenirconnexion().createStatement();
            rs = stm.executeQuery("SELECT e.Etudiant_id, e.nom_prenom, "
                    + "e.annee_naissance,f.filiere_nom,e.promo,e.genre,"
                    + "e.adresse,e.tele,e.email FROM etudiant_info e "
                    + "join filiere f on e.filiere= f.Filiere_id ");

            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("Etudiant_id"), rs.getString("nom_prenom"),
                    rs.getDate("annee_naissance"), rs.getString("filiere_nom"),
                    rs.getDate("promo"), rs.getString("genre"),
                    rs.getString("adresse"), rs.getString("tele"),
                    rs.getString("email")
                });
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
        this.jTable1.setModel(model);
    }

    public final void remplirCbxFiliere() {
        try {
            Statement stmt = cnx.obtenirconnexion().createStatement();
            ResultSet reSet = stmt.executeQuery("SELECT * FROM filiere");
            this.cbxFiliere.removeAllItems();
            while (reSet.next()) {
                this.cbxFiliere.addItem(reSet.getString("filiere_nom"));
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
    }

    public int getIdFiliere(String str) {
        int id;
        try {
            Statement stmt = cnx.obtenirconnexion().createStatement();
            ResultSet resultset = stmt.executeQuery("SELECT * FROM filiere WHERE filiere_nom = '" + str + "'");
            while (resultset.next()) {
                id = resultset.getInt("Filiere_id");
                return id;
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
        return 0;
    }

